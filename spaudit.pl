   } elsif (substr($rest,0,12) eq "SQLException") { #  occured. SQL State = null , SQL Error Code = -4229
      $rest =~ /occured\. (.*)/;
      $tablerun_ref->{sqlex_curr} = $1;
   } elsif (index($rest,"Error for batch element") != -1) { #  com.ibm.db2.jcc.am.SqlIntegrityConstraintViolationException: Error for batch element #1: Assignment of a NULL value to a NOT NULL column "TBSPACEID=2, TABLEID=9994, COLNO=6" is not allowed.. SQLCODE=-407, SQLSTATE=23502, DRIVER=3.64.141
      if ($tablerun_ref->{batch_ex} eq "") {
         $rest =~ / (.*)/;
         $tablerun_ref->{batch_ex} = $rest;
      }
   } elsif (substr($rest,0,10) eq "EXCEPTION:") { #  EXCEPTION: Failed to create aggregates for node: (ctgNode01MXSer:MOPYZ6160187:KYNS)
      $rest =~ /EXCEPTION: (.*)/;
      $tablerun_ref->{excep_curr} = $1;
      if (index($rest,"node") != -1) {
         $rest =~ /node: \((\S+)\)/;
         $tablerun_ref->{excep_node} = $1;
      }
      my @iexcep = [$tablerun_ref->{excep_node},$tablerun_ref->{sqlex_curr},$tablerun_ref->{batch_ex},$tablerun_ref->{excep_curr},$tablerun_ref->{l}];
      push @{$tablerun_ref->{excep}},\@iexcep;
      $tablerun_ref->{excep_node} = "";
      $tablerun_ref->{sqlex_curr} = "";
      $tablerun_ref->{batchex} = "";
      $tablerun_ref->{excep_curr} = "";
   # == 2713 t=work1 work1 - Rows pruned: (0) for table: (Process) and node: (IM-FX-MT-024:KUX) and tmzDiff: (10800)
   }  elsif (substr($restt,0,12) eq "Rows pruned:") { #  Rows pruned: (0) for table: (Process) and node: (IM-FX-MT-024:KUX) and tmzDiff: (10800)
      $restt =~ /Rows pruned: \((\d+)\)/;
      my $iprune = $1;
      $tablerun_ref->{pruned_rows} += $iprune;
      $table_ref = $tablex{$tablerun_ref->{table}};
      $table_ref->{pruned_rows} += $iprune;

   # == 2944 t=work1 work1 - Elapsed time in ms : (536395) for table : (Process) table 1
   }  elsif (substr($restt,0,12) eq "Elapsed time") { # Elapsed time in ms : (536395) for table : (Process) table 1
      $restt =~ /ms : \((\d+)\).*table : \((\S+)\) /;
      my $itime = $1;
      my $itable = $2;
      my $table_ref = $tablex{$tablerun_ref->{table}};
      $table_ref->{table_time} = $itime;
      my $node_ct = scalar keys %{$tablerun_ref->{nodes}};
      $outline  = $tablerun_ref->{table} . ",";
      my $report_ref = $reportx{$tablerun_ref->{table}};
      if (!defined $report_ref) {
         my %reportref = (
                             node_ct =>  $node_ct,
                             aggregate_rows => $tablerun_ref->{aggregate_rows},
                             pruned_rows => $tablerun_ref->{pruned_rows},
                             sqls => $tablerun_ref->{sqls},
                             table_time => $itime,
                             sum_hour => 0,
                             sum_day => 0,
                             sum_week => 0,
                             sum_month => 0,
                             sum_year => 0,
                             prune_raw => 0,
                             prune_hour => 0,
                             prune_day => 0,
                             prune_week => 0,
                             prune_month => 0,
                             prune_year => 0,
                             rowsize => 0,
                             aggsize => 0,
                             cu_aggsize => 0,
                         );
         $report_ref = \%reportref;
         $reportx{$tablerun_ref->{table}} = \%reportref;

      }
      $outline .= $node_ct . ",";
      $outline .= $tablerun_ref->{aggregate_rows} . ",";
      $outline .= $tablerun_ref->{pruned_rows} . ",";
      $outline .= $tablerun_ref->{sqls} . ",";
      $outline .= $itime . ",";
      my $prune_ref = $prunex{$tablerun_ref->{table}};
      if (!defined $prune_ref) {
        my $itable = substr($tablerun_ref->{table},0,-2);
        $prune_ref = $prunex{$itable};
      }
      if (defined $prune_ref) {
         $outline .= $prune_ref->{sum_hour} . ",";
         $outline .= $prune_ref->{sum_day} . ",";
         $outline .= $prune_ref->{sum_week} . ",";
         $outline .= $prune_ref->{sum_month} . ",";
         $outline .= $prune_ref->{sum_year} . ",";
         $outline .= $prune_ref->{prune_raw} . ",";
         $outline .= $prune_ref->{prune_hour} . ",";
         $outline .= $prune_ref->{prune_day} . ",";
         $outline .= $prune_ref->{prune_week} . ",";
         $outline .= $prune_ref->{prune_month} . ",";
         $outline .= $prune_ref->{prune_year} . ",";
         $outline .= $prune_ref->{rowsize} . ",";
         $outline .= $prune_ref->{rowsize} * $tablerun_ref->{aggregate_rows} . ",";
         my $err_ct = 0;
         $err_ct = scalar @{$tablerun_ref->{excep}} if defined $tablerun_ref->{excep};
         $outline .= $err_ct . ",";
         $report_ref->{sum_hour} += $prune_ref->{sum_hour};
         $report_ref->{sum_day} += $prune_ref->{sum_day};
         $report_ref->{sum_week} += $prune_ref->{sum_week};
         $report_ref->{sum_month} += $prune_ref->{sum_month};
         $report_ref->{sum_year} += $prune_ref->{sum_year};
         $report_ref->{prune_raw} += $prune_ref->{prune_raw};
         $report_ref->{prune_hour} += $prune_ref->{prune_hour};
         $report_ref->{prune_day} += $prune_ref->{prune_day};
         $report_ref->{prune_week} += $prune_ref->{prune_week};
         $report_ref->{prune_month} += $prune_ref->{prune_month};
         $report_ref->{prune_year} += $prune_ref->{prune_year};
         $report_ref->{rowsize} += $prune_ref->{rowsize};
         $report_ref->{aggsize} += $prune_ref->{rowsize} * $tablerun_ref->{aggregate_rows};
         $total_aggsize += $prune_ref->{rowsize} * $tablerun_ref->{aggregate_rows};
      }
      print DH "$outline\n";

      if (defined $tablerun_ref->{excep}) {
         my $err_ct = scalar @{$tablerun_ref->{excep}} if defined $tablerun_ref->{excep};
         if ($err_ct > 0) {
            for (my $i = 0; $i < $err_ct; $i++) {
               $outline = $itable . ",";
               $outline .= $tablerun_ref->{excep}[$i][0][0] . ",";
               $outline .= $tablerun_ref->{excep}[$i][0][4] . ",";
               $outline .= $tablerun_ref->{excep}[$i][0][1] . ",";
               $outline .= $tablerun_ref->{excep}[$i][0][2] . ",";
               $outline .= $tablerun_ref->{excep}[$i][0][3] . ",";
               print EH "$outline\n";
            }
         }
      }
      delete $tablerunx{$ithread};
   }
}
close DH;
